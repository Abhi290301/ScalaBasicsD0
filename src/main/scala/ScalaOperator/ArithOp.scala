package ScalaOperator

object ArithOp {
  def main(args: Array[String]) {
    // variables
    var a = 25
    var b = 23

    // Addition
    println("Addition of a + b = " + (a + b))

    // Subtraction
    println("Subtraction of a - b = " + (a - b))

    // Multiplication
    println("Multiplication of a * b = " + (a * b))

    // Division
    println("Division of a / b = " + (a / b))

    // Modulus
    println("Modulus of a % b = " + (a % b))

  }
}
